//switch case
let day=4
switch(day) {
  case "monday":
    console.log("Print day as monday")
    break;
    case "tuesday":
    console.log("Print day as tuesday")
    break;
    case "wednesday":
    console.log("Print day as wednesday")
    break;
    case "thursday":
    console.log("Print day as thursday")
    break;
    case "friday":
    console.log("Print day as friday")
    break;
    default:
    console.log("Print day as Not valid")
    break;
}